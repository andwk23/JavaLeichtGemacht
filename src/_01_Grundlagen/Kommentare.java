package _01_Grundlagen;

/**
 * 
 * Kommentare bei Java
 * Dokumentationskommentar
 * 
 */

public class Kommentare {

	public static void main(String[] args) {
		
		System.out.println("\n*** Es gibt in Java 3 Arten von Kommentaren ***");
		System.out.println("-----------------------------------------------\n");
		System.out.println("--- Dokumentationskommentar: /** * * */ ---");
		System.out.println("\n--- Zeilenkommentar: // ---");
		System.out.println("\n--- Blockkommentarkommentar: /* * * */ ---");
		
		// Zeilenkommentar
		
		/*
		 * 
		 * Blockkommentar
		 * 
		 */

	}

}
