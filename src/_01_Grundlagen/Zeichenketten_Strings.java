package _01_Grundlagen;

public class Zeichenketten_Strings {

	public static void main(String[] args) {
		
		System.out.println("\n*** Die Klasse String ***");
		System.out.println("-------------------------\n");
		
		String stringBeispiel = "Ich bin eine Zeichenkette";
		String name = "Wiwi";
		String age = "68";
		char ausrufezeichen = '!';
		
		System.out.println("\tBeispielzeichenkette:\n\t" + stringBeispiel);
		System.out.println("\n\tZusammengesetzte Zeichenkette:\n\t" + name + " " + age + " " + ausrufezeichen);

	}

}
